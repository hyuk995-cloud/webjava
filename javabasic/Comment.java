package javabasic;

public class Comment {
    public static void main(String[] args) {
        System.out.println("=== 주석 예제 ===");
        System.out.println("이름 : 홍길동");
        System.out.println("나이 : 31세");
        // 이스케이프 문자 확인
        System.out.println("줄 바꿈\n확인");
        System.out.println("탭\t간격확인");
        System.out.println("\" 큰 따옴표 출력");
        System.out.println("\' 작은 따옴표 출력");
        System.out.println("\\ 역 슬래시 \\ 출력");
    }
}
